/*
PrinterExample 클래스에서 Printer 객체 생성하고 println() 메소드 호출해서 매개값을
콘솔에 출력하려고 함
println() 메소드 매개값으로 int, boolean, double, String 값 줄 수 있음
println() 메소드 선언해보기
 */

public class Printer {
    // 필드
//    int num;
//    boolean tf;
//    double numSpot;
//    String name;

    // 메소드
    static void println(int value) {
        System.out.println(value);
    }
    static void println(boolean value) {
        System.out.println(value);
    }
    static void println(double value) {
        System.out.println(value);
    }
    static void println(String value) {
        System.out.println(value);
    }
}
