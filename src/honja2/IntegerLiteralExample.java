package Honja2;

public class IntegerLiteralExample {
    public static void main(String[] args) {
        int var1 = 0b1011;      // 2진슈
        int var2 = 0206;      // 8진슈
        int var3 = 365;      // 10진슈
        int var4 = 0xB3;      // 16진슈

        System.out.println("var1 : " + var1);
        System.out.println("var2 : " + var2);
        System.out.println("var3 : " + var3);
        System.out.println("var4 : " + var4);
    }
}
